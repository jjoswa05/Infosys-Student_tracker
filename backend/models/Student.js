class Student {
  constructor(id, name, registerNumber, department, year, section, email, phone) {
    this.id = id;
    this.name = name;
    this.registerNumber = registerNumber;
    this.department = department;
    this.year = year;
    this.section = section;
    this.email = email;
    this.phone = phone;
  }
}

module.exports = Student;
